package if_else_operation;

public class Example4 {

	public static void main(String[] args) {
    char ch='o';
    if(ch=='a'||ch=='A') { 
    	System.out.println("char is vowel"+ch );
    }else if(ch=='e'||ch=='E') {
    	System.out.println("char is vowel"+ch );
    }else if(ch=='i'||ch=='I') {
    	System.out.println("char is vowel"+ch );
	}else if(ch=='o'||ch=='O') {
    	System.out.println("char is vowel"+ch );
    }else if(ch=='u'||ch=='U') {
    	System.out.println("char is vowel"+ch );
    } else {
    	System.out.println("char is vowel"+ch );
    }   
	}}
